	computed: {



	},
	created() {
		this.ruleForm.dengjishijian = this.getCurDate()
	},
	methods: {
		
		// 下载
		download(file){
			window.open(`${file}`)
		},
		// 初始化
		init(id,type) {
			if (id) {
				this.id = id;
				this.type = type;
			}
			if(this.type=='info'||this.type=='else'){
				this.info(id);
			}else if(this.type=='logistics'){
				this.logistics=false;
				this.info(id);
			}else if(this.type=='cross'){
				var obj = this.$storage.getObj('crossObj');
				for (var o in obj){
						if(o=='chufabianhao'){
							this.ruleForm.chufabianhao = obj[o];
							this.ro.chufabianhao = true;
							continue;
						}
						if(o=='xueshengzhanghao'){
							this.ruleForm.xueshengzhanghao = obj[o];
							this.ro.xueshengzhanghao = true;
							continue;
						}
						if(o=='xueshengxingming'){
							this.ruleForm.xueshengxingming = obj[o];
							this.ro.xueshengxingming = true;
							continue;
						}
						if(o=='xuefen'){
							this.ruleForm.xuefen = obj[o];
							this.ro.xuefen = true;
							continue;
						}
						if(o=='jianxuefenyuanyin'){
							this.ruleForm.jianxuefenyuanyin = obj[o];
							this.ro.jianxuefenyuanyin = true;
							continue;
						}
						if(o=='dengjishijian'){
							this.ruleForm.dengjishijian = obj[o];
							this.ro.dengjishijian = true;
							continue;
						}
						if(o=='suguanzhanghao'){
							this.ruleForm.suguanzhanghao = obj[o];
							this.ro.suguanzhanghao = true;
							continue;
						}
						if(o=='suguanxingming'){
							this.ruleForm.suguanxingming = obj[o];
							this.ro.suguanxingming = true;
							continue;
